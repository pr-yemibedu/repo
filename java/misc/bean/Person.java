public class Person
{
	private String last = null;
	private String first = null;
	
	public Person()
	{
	}

	public void setLast(String last)
	{
		this.last = last;
	}
	
	public String getLast()
	{
		return this.last;
	}
	
	public void setFirst(String first)
	{
		this.first = first;
	}
	
    public String getFirst()
	{
        return this.first;
    }
}